-- a block-eat-block game
-- by vlek

-- init vars
game = {}
tick = 0
blink = true

function _init()
  game_menu()
end

function _update()
  game._update()
end

function _draw()
  game._draw()
end

function game_menu()
  sfx(2,2)
  game._update = _update_menu
  game._draw = _draw_menu
end

function _update_menu()
  if btnp(4) then start_game() end
  do_tick()
end

function _draw_menu()
  cls()
  sspr(0,0,32,32, 5,-5,135,125)
  if blink then print("press 'z' to play!",30,115) end
end

function start_game()
  sfx(-1,2)
  player = {}
  player.x = 64
  player.y = 64
  player.size = 2
  blocks = {}
  game._update = _update_game
  game._draw = _draw_game
end

function _update_game()
  if player.size >= 52 then
    game_win()
  end
  -- if under block limit, possibly create more!
  -- 20% chance of a new block per check
  if #blocks < 10 and flr(rnd(6)) == 1 then
    add(blocks, create_block())
  end
  -- for every block, check for collisions and move them
  for block in all(blocks) do
    if (collision(player.x,player.y,
                  player.x+player.size, player.y+player.size,
                  block.x,block.y,
                  block.x+block.size, block.y+block.size)) then
      if block.size >= player.size then
        sfx(1)
        player.size = 2
      else
        sfx(0)
        player.size += 1
      end
      del(blocks, block)
    elseif -1*block.size > block.x or block.x > 127 then
      del(blocks, block)
    else
      block.x += block.speed * block.dir
    end
  end
  -- perform movement: usually movement goes first, but, in this case,
  -- it is performed last due to the possibility of the player moving
  -- out of the way of a block at the last concievable 1/30th of a second.
  if btn(0) and player.x > 1 then player.x-=1 end
  if btn(1) and player.x + player.size < 126 then player.x+=1 end
  if btn(2) and player.y > 1 then player.y-=1 end
  if btn(3) and player.y + player.size < 126 then player.y+=1 end
end

function _draw_game()
  cls()
  for block in all(blocks) do
    rectfill(block.x,block.y,block.x+block.size,block.y+block.size,8)
  end
  rectfill(player.x,player.y,player.x+player.size,player.y+player.size,10)
  -- draw the edge deliminator last
  -- so things don't draw on top of it.
  rect(0,0,127,127,1)
  -- add the player's score to the top-left of the screen
  print(player.size-2,4,4)
end

function game_win()
  game._update = _update_win
  game._draw = _draw_win
end

function _update_win()
  if btnp(4) then start_game() end
  do_tick()
end

function _draw_win()
  cls()
  print('you win!',50,40)
  if blink then print("press 'z' to play again!",17,80) end
end

function create_block()
  -- returns a block enemy
  local block = {}
  block.size = flr(rnd(15)) + 1
  if flr(rnd(2)) == 1 then
    block.dir = 1
    block.x = -1 * block.size
  else
    block.dir = -1
    block.x = 127
  end
  block.y = flr(rnd(127)) -- - block.size
  block.speed = flr(rnd(2))+1
  return block
end

function in_range(x1,x2,x3)
  -- helper function for the collision detection system
  return x3 >= x1 and x3 <= x2
end

function collision(x1,y1,x2,y2,x3,y3,x4,y4)
  -- this uses the values for the top-left and bottom-right corners of two given squares
  -- and performs a check to see if any of the edges of one square are within the x,y ranges
  -- of the second to figure out whether they're touching or not.
  if ((in_range(x1,x2,x3) or in_range(x1,x2,x4) or in_range(x3,x4,x1) or in_range(x3,x4,x2)) and
      (in_range(y1,y2,y3) or in_range(y1,y2,y4) or in_range(y3,y4,y1) or in_range(y3,y4,y2))) then
    return true
  end
  return false
end

function do_tick()
  if tick < 29 then
    tick += 1
  else
    tick = 0
    if blink then
      blink = false
    else
      blink = true
    end
  end
end

