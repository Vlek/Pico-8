-- init vars
game = {}
tick = 0
blink = true

function _init()
  game_menu()
end

function _update()
  game._update()
end

function _draw()
  game._draw()
end

function game_menu()
  game._update = _update_menu
  game._draw = _draw_menu
end

function _update_menu()
  if btnp(4) then start_game() end
  do_tick()
end

function _draw_menu()
  cls()
  sspr(0,0,56,32, 0,-5,132,99)
  if blink then print("press 'z' to play!",30,105) end
end

function start_game()
  player = {}
  player.x = 8
  player.y = 8
  player.direction = 0
  player.tail_additions = 4
  board = {{player.x,player.y}}
  tick_counter = 0
  food = create_food()
  game._update = _update_game
  game._draw = _draw_game
end

function _update_game()
  --perform movement:
  if btnp(0) and player.direction != 1 then player.direction = 0 end
  if btnp(1) and player.direction != 0 then player.direction = 1 end
  if btnp(2) and player.direction != 3 then player.direction = 2 end
  if btnp(3) and player.direction != 2 then player.direction = 3 end
  if tick_counter > 3 then
    -- we will perform as much as possible in the tick counter
    -- so as to be as computationally economical considering
    -- the player won't move outside of a tick.
    tick_counter = 0
    if player.direction == 0 then
      player.x -= 1
    elseif player.direction == 1 then
      player.x += 1
    elseif player.direction == 2 then
      player.y -= 1
    else
      player.y += 1
    end
    -- check if the player is now out-of-bounds
    if player.x < 0 or player.x > 15 or player.y < 0 or player.y > 15 then
      game_lose()
    end
    -- check if the player managed to eat themselves
    for y=0,15,1 do
      for x=0,15,1 do
        good_location = true
        for i=1,#board,1 do
          if board[i][1] == player.x and board[i][2] == player.y then
            game_lose()
            break
          end
        end
      end
    end
    -- check if player eats food
    if food[1] == player.x and food[2] == player.y then
      sfx(0,0)
      food = create_food()
      player.tail_additions += 3
    end
    -- here's where we add the new part in the direction the player
    -- is going if they do not have any more growing to do from eating
    -- also, fifo the snake body to shift it forward where necessary
    add(board, {player.x,player.y})
    if player.tail_additions > 0 then
      player.tail_additions -= 1
    else
      del(board, board[1])
    end
  else
    tick_counter += 1
  end
end

function _draw_game()
  cls()
  -- display food
  if food then
    draw_cube(food[1],food[2],8)
  end
  -- display the user
  for snake_part in all(board) do
    draw_cube(snake_part[1],snake_part[2],10)
  end
  print(#board,6,6)
  rect(0,0,127,127,1)
end

function game_win()
  game._update = _update_win
  game._draw = _draw_win
end

function _update_win()
  if btnp(4) then start_game() end
  do_tick()
end

function _draw_win()
  cls()
  print('you win!',50,25)
  if blink then print("press 'z' to play again!",17,50) end
  sspr(56,0,24,24, 30,55,72,72)
end

function game_lose()
  game._update = _update_lose
  game._draw = _draw_lose
end

function _update_lose()
  if btnp(4) then start_game() end
  do_tick()
end

function _draw_lose()
  cls()
  print('game over',64-#'game over'*4/2,25)
  if blink then print("press 'z' to play again!",17,50) end
  sspr(80,0,32,24, 15,60,96,72)
end

function draw_cube(x,y,color)
  block_x_start = x * 8 + 2
  block_y_start = y * 8 + 2
  rectfill(block_x_start,block_y_start,block_x_start+3,block_y_start+3,color)
end

function create_food()
  -- returns a food object
  -- create a list of all possible food locations
  possible_food_locations = {}
  for y=0,15,1 do
    for x=0,15,1 do
      good_location = true
      for i=1,#board,1 do
        if board[i][1] == x and board[i][2] == y then
          good_location = false
          break
        end
      end
      if good_location then
        add(possible_food_locations, {x,y})
      end
    end
  end
  return possible_food_locations[flr(rnd(#possible_food_locations))+1]
end

function do_tick()
  if tick < 29 then
    tick += 1
  else
    tick = 0
    if blink then
      blink = false
    else
      blink = true
    end
  end
end

