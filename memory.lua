__lua__
-- sound-based memory
-- by vlek, dedicated to tom g.

game = {}
tick = 0
blink = true

function _init()
  game_menu()
end

function _update()
  game._update()
end

function _draw()
  game._draw()
end

function game_menu()
  game._update = menu_update
  game._draw = menu_draw
end

function menu_update()
  if btnp(4) then play_game() end
  do_tick()
end

function menu_draw()
  cls()
  -- todo: trade this out for a real graphic
  sspr(0,0,32,24, -5,0,132,99) -- 0,0,118,94)
  if blink then print("press 'z' to play!",25,105) end
end

function play_game()
  -- init vars
  current_x = 1
  current_y = 1
  button_size = 15
  button_offset = flr((126 - (button_size * 4)) / 5)
  -- create the buttons and place them in the board table.
  local sounds = {}
  for s = 0, 7, 1 do
    for e = 0, 1, 1 do
      add(sounds, s)
    end
  end
  board = {}
  for y = 1, 4, 1 do
    for x = 1, 4, 1 do
      local button = {}
      button.x = x
      button.y = y
      button.last_played = false
      button.solved = false
      local sound_num = sounds[flr(rnd(#sounds))+1]
      button.sound = sound_num
      del(sounds, sound_num)
      add(board, button)
    end
  end
  game._update = game_update
  game._draw = game_draw
end

function game_update()
  -- only allow button presses if there is no sound playing
  if (stat(16) == -1) do
    if (btnp(0) and current_x > 1) then current_x -= 1 end
    if (btnp(1) and current_x < 4) then current_x += 1 end
    if (btnp(2) and current_y > 1) then current_y -= 1 end
    if (btnp(3) and current_y < 4) then current_y += 1 end
    if (btnp(4)) then
      local b = get_current_button()
      -- only allow the button press if it wasn't the last played
      -- and hasn't already been solved
      if not b.last_played and not b.solved then
        sfx(b.sound,0)
        local last_b = get_last_button()
        if last_b then
          if last_b.sound == b.sound then
            last_b.solved = true
            b.solved = true
            -- check for win
            local win = true
            for b in all(board) do
              if not b.solved then
                win = false
                break
              end
            end
            if win then
              game_win()
            end
          end
          last_b.last_played = false
        else
          b.last_played = true
        end
      end
    end
  end
end

function game_draw()
  cls()
  rectfill(0,0,128,128,5)
  -- print(#board)
  for b in all(board) do
    local color = 8
    -- if it's the case that we're playing a sound, we have an active last button,
    -- or there's a solved button, then draw the button as white instead of red.
    if stat(16) ~= -1 and (current_x == b.x and current_y == b.y) or
       b.last_played == true or b.solved then
      color = 7
    end
    local start_x = (b.x * button_offset) + ((b.x - 1) * button_size)
    local start_y = (b.y * button_offset) + ((b.y - 1) * button_size)
    rectfill(start_x,start_y,start_x+button_size,start_y+button_size,color)
    -- if you want to cheat and see what button is what sound, uncomment below:
    -- print(b.sound,start_x,start_y,2)
    -- add selector rectangle where necessary:
    if current_x == b.x and current_y == b.y then
      rect(start_x-2,start_y-2,start_x+button_size+2,start_y+button_size+2,2)
    end
  end
end

function get_current_button()
  -- returns the button that the player is currently on
  for b in all(board) do
    if b.x == current_x and b.y == current_y then
      return b
    end
  end
end

function get_last_button()
  -- returns the last played button
  for b in all(board) do
    if b.last_played then
      return b
    end
  end
  return false
end

function game_win()
  game._update = update_win
  game._draw = draw_win
end

function update_win()
  if btnp(4) then play_game() end
  if tick == 0 then
    local toggle = true
    if board[1].solved == true then
       toggle = false
    end
    for b in all(board) do
      b.solved = toggle
    end
  end
  do_tick()
end

function draw_win()
  -- we're going to be sneaky and just re-draw using the normal game
  -- after fudging with the solved vars for each button to make them blink
  -- unfortunately, because of this, we can't draw anything beforehand because
  -- game_draw does a cls()
  game_draw()
  color(2)
  print('you win!',50,61,2)
  if blink then print("press 'z' to play again!",17,118,2) end
end

function do_tick()
  if tick < 29 then
    tick += 1
  else
    tick = 0
    if blink then
      blink = false
    else
      blink = true
    end
  end
end

